/**
 * @author: Geoffrey Duncan O.
 * Autoboxing: Go from a primitive data type to wrapper class.
 * Unboxing: Or a wrapper class to primitive data type with relative ease in Java.
 */

public class AutoboxingUnboxing {
    public static void main(String[] args){
        // Autoboxing
        Integer boxedInt = 15;

        // Unboxing
        int unboxedInt = boxedInt;
    }
}
